class Bike {
  final String name;
  final String mileage;
  final int mileageForChart;
  final double topSpeed;
  final String engine;
  final double power;
  final String rental;
  final String image;

  Bike({
    required this.name,
    required this.mileage,
    required this.mileageForChart,
    required this.topSpeed,
    required this.engine,
    required this.power,
    required this.rental,
    required this.image,
  });
}
